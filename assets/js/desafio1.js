imagen = document.querySelector('#invitacion');
imagen.onclick = function() {
    imagen = document.querySelector('#invitacion');
    if (imagen.style.borderStyle == 'solid') {
        imagen.style.border='none'
    } else {
        imagen.style.border='2px solid red'
    }
};


